    private void btnDeletarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeletarActionPerformed
        // TODO add your handling code here:
        if (TabelaProduto.getSelectedRowCount() >= 1) {
            int deleteItem = JOptionPane.showConfirmDialog(null, "Deseja mesmo Excluir Produto",
                    "Warning", JOptionPane.YES_NO_OPTION);
            if (deleteItem == JOptionPane.YES_OPTION) {
                Produto p = new Produto();
                p.setId(Integer.parseInt(TabelaProduto.getValueAt(TabelaProduto.getSelectedRow(), 0).toString()));
                try {
                    produtoDAO.deletarProduto(p);
                    Content panel = new Content(menu);
                    menu.abrirMDI(panel.getFondo());
                } catch (Exception ex) {
                    Logger.getLogger(Content.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
        }

    }//GEN-LAST:event_btnDeletarActionPerformed

