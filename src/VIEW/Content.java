    public void ListarValores() {
        try {
            DefaultTableModel model = (DefaultTableModel) TabelaProduto.getModel();
            model.setNumRows(0);

            ArrayList<Produto> lista = produtoDAO.listarProduto();

            for (int num = 0; num < lista.size(); num++) {
                model.addRow(new Object[]{
                    lista.get(num).getId(),
                    lista.get(num).getNome(),
                    lista.get(num).getData_cadastro(),
                    lista.get(num).getPreco_venda(),
                    lista.get(num).getPreco_compra(),
                    lista.get(num).getQtd_estoque(),
                    lista.get(num).getFator(),
                    lista.get(num).getCodigo_barra(),});

            }
        } catch (Exception erro) {
            JOptionPane.showMessageDialog(null, "ListarValores" + erro);
        }
    }

    public void ListarValoresPeloNome(String nome) {
        try {

            DefaultTableModel model = (DefaultTableModel) TabelaProduto.getModel();

            model.setNumRows(0);

            ArrayList<Produto> lista = produtoDAO.listarProdutoNome(nome);

            for (int num = 0; num < lista.size(); num++) {
                model.addRow(new Object[]{
                    lista.get(num).getId(),
                    lista.get(num).getNome(),
                    lista.get(num).getData_cadastro(),
                    lista.get(num).getPreco_venda(),
                    lista.get(num).getPreco_compra(),
                    lista.get(num).getQtd_estoque(),
                    lista.get(num).getFator(),
                    lista.get(num).getCodigo_barra(),});

            }
        } catch (Exception erro) {
            JOptionPane.showMessageDialog(null, "ListarValores" + erro);
        }
    }

